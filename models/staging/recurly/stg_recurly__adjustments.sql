       A.ACCOUNT:company::STRING                         AS COMPANY,
       A.ACCOUNT: EMAIL::STRING                          AS EMAIL,
       A.ACCOUNT: FIRST_NAME::STRING                     AS FIRST_NAME,
       A.ACCOUNT: LAST_NAME::STRING                      AS LAST_NAME,
       A.ACCOUNT:parent_account_id::STRING               AS PARENT_ACCOUNT_ID,
       A.ACCOUNTING_CODE,
       A.CREATED_AT,
       A.CURRENCY,
       A.DESCRIPTION,
       A.END_DATE,
       A.ID,
       A.INVOICE_ID,
       A.INVOICE_NUMBER,
       A.LEGACY_CATEGORY,
       A.OBJECT,
       A.ORIGIN,
       A.ORIGINAL_LINE_ITEM_INVOICE_ID,
       A.PLAN_CODE,
       A.PREVIOUS_LINE_ITEM_ID,
       A.PRODUCT_CODE,
       A.QUANTITY,
       A.REFUND,
       A.START_DATE,
       A.STATE,
