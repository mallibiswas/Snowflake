       NVL(A.CREDIT_APPLIED, A.CREDIT_APPLIED__DE)       AS CREDIT_APPLIED,
       NVL(A.SUBTOTAL, A.SUBTOTAL__DE)                   AS SUBTOTAL,
       NVL(A.REFUNDED_QUANTITY, A.REFUNDED_QUANTITY__IT) AS REFUNDED_QUANTITY,
       A.UPDATED_AT,
       A.UUID
